package com.trump.auction.web.vo;

public class AccountVo {
 private String auctionCoin ;
 private String presentCoin ;
 private String points;
 private String shoppingCoin;
 private String freezeCoin;

 public String getAuctionCoin() {
  return auctionCoin;
 }

 public void setAuctionCoin(String auctionCoin) {
  this.auctionCoin = auctionCoin;
 }

 public String getPresentCoin() {
  return presentCoin;
 }

 public void setPresentCoin(String presentCoin) {
  this.presentCoin = presentCoin;
 }

 public String getPoints() {
  return points;
 }

 public void setPoints(String points) {
  this.points = points;
 }

 public String getShoppingCoin() {
  return shoppingCoin;
 }

 public void setShoppingCoin(String shoppingCoin) {
  this.shoppingCoin = shoppingCoin;
 }

 public String getFreezeCoin() {
  return freezeCoin;
 }

 public void setFreezeCoin(String freezeCoin) {
  this.freezeCoin = freezeCoin;
 }
}
